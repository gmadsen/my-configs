function M.config()
  local Hydra = require("hydra")

  -- This Hydra lets you scroll sideways is a sane way

  Hydra({
    name = "Side scroll",
    mode = "n",
    body = "z",
    heads = {
      { "h", "5zh" },
      { "l", "5zl", { desc = "←/→" } },
      { "H", "zH" },
      { "L", "zL", { desc = "half screen ←/→" } },
    },
  })
end

return M

